    def build(self, jeu):
        jeu.fenetre.fill((0, 0, 0))
        # jeu.fenetre.blit(pygame.image.load(f"assets/background/{self.lvl}.png"))
        for l in range(len(self.tiles)):
            for t in range((len(self.tiles[0]))):
                jeu.fenetre.blit(
                    pygame.transform.scale(pygame.image.load(f"assets/tiles/{self.tiles[l][t]}.png"),
                                           (16 * scale, 16 * scale)),
                    (t * 16 * scale, l * 16 * scale))
                if self.tiles[l][t] != 0:
                    # create colidesboxes
                    self.collides.append(pygame.Rect(t * 16 * scale, l * 16 * scale, 16 * scale, 16 * scale))

        print(self.collides)
        pygame.display.flip()
